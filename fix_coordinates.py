rest_of_file = content[zones_json_end:]
header = content[:zones_json_start]

fixed_count = 0
zone_fix_count = 0
missing = []

for zone in zones:
    # Fix zone center if needed
    if zone['id'] in ZONE_CENTER_FIXES:
        fix = ZONE_CENTER_FIXES[zone['id']]
        old_center = zone['center']
        zone['center'] = fix['center']
        zone['radius'] = fix['radius']
        zone_fix_count += 1
        print(f"[ZONE FIX] {zone['name']}: center {old_center} -> {fix['center']}, radius {fix['radius']}")
    
    # Fix POI coordinates
    if 'pois' not in zone:
        continue
    for cat, pois in zone['pois'].items():
        for poi in pois:
            if poi['name'] in KNOWN_COORDS:
                old_coords = poi['coords']
                new_coords = KNOWN_COORDS[poi['name']]
                if old_coords != new_coords:
                    poi['coords'] = new_coords
                    fixed_count += 1
                    # Only print significant changes
                    dist_lng = abs(old_coords[0] - new_coords[0])
                    dist_lat = abs(old_coords[1] - new_coords[1])
                    if dist_lng > 0.01 or dist_lat > 0.01:
                        print(f"  [BIG FIX] {poi['name']}: {old_coords} -> {new_coords}")
            else:
                missing.append(f"{zone['name']}/{poi['name']}")

print(f"\n=== Summary ===")
print(f"Zone centers fixed: {zone_fix_count}")
print(f"POI coordinates fixed: {fixed_count}")
if missing:
    print(f"POIs not in KNOWN_COORDS ({len(missing)}): {', '.join(missing[:10])}...")

# Write back
new_content = header + json.dumps(zones, ensure_ascii=False, indent=2) + rest_of_file
with open('/home/user/workspace/beijing-metro-tod/data.js', 'w', encoding='utf-8') as f:
    f.write(new_content)

print("\ndata.js updated successfully!")
